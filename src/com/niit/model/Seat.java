package com.niit.model;

public class Seat {
   String id;
   String seatNo;
   String cost;
   
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getSeatNo() {
	return seatNo;
}
public void setSeatNo(String seatNo) {
	this.seatNo = seatNo;
}
public String getCost() {
	return cost;
}
public void setCost(String cost) {
	this.cost = cost;
}
}
